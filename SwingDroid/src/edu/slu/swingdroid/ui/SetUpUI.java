    private void nextButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nextButtonActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) subjectTable.getModel();
        if (model.getRowCount() > 0) {
            model = (DefaultTableModel) subjectTable.getModel();
            List<Subject> newList = new LinkedList<>();
            for (Object row : model.getDataVector()) {
                Subject toAdd = new Subject(((Vector) row).get(0).toString());
                newList.add(toAdd);
            }
            Subjects.getInstance().updateList(newList);
            this.setVisible(false);
            new HomeUserInterface().setVisible(true);

        } else {
            JOptionPane.showMessageDialog(this, "Please insert subjects...",
                    "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_nextButtonActionPerformed

