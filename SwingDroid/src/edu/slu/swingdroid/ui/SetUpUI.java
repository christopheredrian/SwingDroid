    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        List<String> subjectsInside = new ArrayList<>();
        if (tableModel.getRowCount() > 0) {
            Vector vector = tableModel.getDataVector();
            for (Object row : vector) {
                Vector current = (Vector) row;
                subjectsInside.add((String) current.get(0));
            }
        }
        if (UIUtilities.validateSubjectInput(subjectTextField.getText())
                && !subjectsInside.contains(subjectTextField.getText())) {
            tableModel.insertRow(tableModel.getRowCount(), new Object[]{subjectTextField.getText()});
        } else {
            JOptionPane.showMessageDialog(this, "Invalid subject name...",
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_addButtonActionPerformed

